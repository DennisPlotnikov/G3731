int CountElem(int [] arr)
{
    int res = 0;
    for (int i = 0; i < arr.Length; i++)
    {
        if (Test(arr[i]))
        {
            res++;
        }
    }
    return res;
}

bool Test(int n)
{
    return ((n > 10)&&(n < 99));
}

int[] arr = Gen1DArr(5, 0, 199);

int result = CountElem(arr);

